def import_companies(csv_path: str = None, industry_filter: str = None, skip_existing: bool = True):
    """批量导入企业"""
    init_db()
    db = SessionLocal()

    if csv_path:
        # 从 CSV 文件导入
        companies = _load_from_csv(csv_path)
    else:
        # 使用内置列表
        companies = BUILTIN_COMPANIES

    if industry_filter:
        companies = [c for c in companies if len(c) >= 3 and c[2] == industry_filter]

    # 过滤金融类
    companies = [c for c in companies if len(c) >= 3 and c[2] not in FINANCIAL_INDUSTRIES]

    # 去重（按股票代码，保留第一个）
    seen_codes = set()
    deduped = []
    for c in companies:
        code = c[0]
        if code not in seen_codes:
            seen_codes.add(code)
            deduped.append(c)
    companies = deduped

    existing_codes = {c.stock_code for c in db.query(Company).all()}
    new_count = 0
    skipped = 0
    st_count = 0

    for comp in companies:
        code = comp[0]
        name = comp[1]
        industry = comp[2]
        is_st = len(comp) >= 4 and comp[3]

        if skip_existing and code in existing_codes:
            skipped += 1
            continue

        company = Company(
            stock_code=code,
            company_name=name,
            industry=industry,
            short_name=name,
            is_a_share=True,
            is_seed=False,
            is_st=is_st,
            is_active=not is_st,
        )
        db.add(company)
        new_count += 1
        if is_st:
            st_count += 1

    db.commit()
    total = db.query(Company).count()
    db.close()

    print(f"✅ 导入完成！")
    print(f"   新增: {new_count} 家")
    print(f"   跳过: {skipped} 家（已存在）")
    print(f"   其中 ST: {st_count} 家（标记为不活跃，不会进入分析）")
    print(f"   数据库总计: {total} 家")
    print(f"   涵盖行业: {len(set(c[2] for c in companies if len(c) >= 3))} 个")


def _load_from_csv(path: str) -> list:
    """从 CSV 文件加载企业列表
    CSV 格式: 股票代码,企业名称,行业[,is_st]
    """
    companies = []
    with open(path, "r", encoding="utf-8") as f:
        reader = csv.reader(f)
        header = next(reader, None)  # 跳过表头
        for row in reader:
            if len(row) >= 3:
                code = row[0].strip()
                name = row[1].strip()
                industry = row[2].strip()
                is_st = len(row) >= 4 and row[3].strip().upper() in ("TRUE", "1", "YES", "ST")
                companies.append((code, name, industry, is_st))
    return companies


